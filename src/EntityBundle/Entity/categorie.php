<?php

namespace EntityBundle\Entity;

/**
 * categorie
 */
class categorie
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $titre;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set titre
     *
     * @param string $titre
     *
     * @return categorie
     */
    public function setTitre($titre)
    {
        $this->titre = $titre;
    
        return $this;
    }

    /**
     * Get titre
     *
     * @return string
     */
    public function getTitre()
    {
        return $this->titre;
    }
}

