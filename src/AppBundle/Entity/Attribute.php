<?php

namespace AppBundle\Entity;

/**
 * Attribute
 */
class Attribute
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var int
     */
    private $idCategorie;

    /**
     * @var string
     */
    private $name;

    /**
     * @var int
     */
    private $type;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set idCategorie
     *
     * @param integer $idCategorie
     *
     * @return Attribute
     */
    public function setIdCategorie($idCategorie)
    {
        $this->idCategorie = $idCategorie;
    
        return $this;
    }

    /**
     * Get idCategorie
     *
     * @return integer
     */
    public function getIdCategorie()
    {
        return $this->idCategorie;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Attribute
     */
    public function setName($name)
    {
        $this->name = $name;
    
        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set type
     *
     * @param integer $type
     *
     * @return Attribute
     */
    public function setType($type)
    {
        $this->type = $type;
    
        return $this;
    }

    /**
     * Get type
     *
     * @return integer
     */
    public function getType()
    {
        return $this->type;
    }
}

